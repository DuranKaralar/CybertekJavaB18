package day18_NestedLoop;

public class do_While_Loop2 {
    public static void main(String[] args) {

        int number=103;// print the number between number - 100
        while(number>=100){
            System.out.println(number);// in order to find untill 100 we need to give iteration
            number--;
        }

        int number2=103;

        do{
            System.out.println(number2); // it give us the same result with the while loop if the condition is true
            number2--;
        }while(number2>=100);// when the condition is false it will print only the number ot the satetment just once















    }
}
